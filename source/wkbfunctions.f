      function Fmoment(Eps)
C
C     Integrand (To be called from Gauss-Legendre integration routine)
C
C     To be defined as external function
C
      IMPLICIT NONE
      real Fmoment
      real Eps, Vdef
      include "wkb.cmb"
      Fmoment = 2.d0*SMIU* sqrt( abs (UEXC - Vdef(Eps)) )
      return
      end
